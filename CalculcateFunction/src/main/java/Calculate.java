public class Calculate {

    public Double calculateSum(double x, double y){
        return x+y;
    }


    public Double calculateMultiple(double x, double y){
        return x*y;
    }


    public Double calculateDivide(double x, double y){
        return x/y;
    }

    public Double calculateMinus(double x, double y){
        return x-y;
    }
}
